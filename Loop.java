
public class Loop {
    public static void main(String[] args) {
        System.out.println("For loop");
        //for loop
        for (int i = 0; i <= 5; i++) {
            System.out.print(i+" ");
        }
        System.out.println(" ");
        System.out.println("While Loop");
        //while loop
        int i = 0;
        while (i <= 5) {
            System.out.print(i+" ");
            i++;
        }
    }
}